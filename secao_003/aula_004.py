#Tipos int e float

#int -> numero inteiro
#O tipo int representa qualquer numero
#positivo ou negativo, inte sem sinal é considerado positivo
print("Valores Int")
print(10)
print(-10)
print(0)
print()

#float -> numero com ponto flutuante
#O tipo float representa qualquer numero
#positivo ou negativo com ponto flutuante
#float sem sinal é considerado positivo
print("Valores float")
print(10.0)
print(-10.0)
print(0.0 )
print()

#A funnção type mostra o tipo que o Python inferiu ao valor
print("Type valores")
print(type(1.5))
print(type(1))
print(type(True))
print(type('Vinicius'))